import { NextResponse } from 'next/server'
import prisma from '@/lib/prisma'
import { ErrorCode } from '@/lib/errors';

export async function POST(request: Request) {

  const body = await request.json();
  const { address, status, flowTypeArr, cursor, take } = body;

  if (!address.toLowerCase()) {
    return NextResponse.json({ error: ErrorCode.MISSING_WALLET_ADDRESS }, { status: 400 })
  }

  // Get where clause for both queries
  const whereClause = {
    user_address: address.toLowerCase(),
    ...(status && {
      status: {
        in: status
      }
    }),
    ...(flowTypeArr && {
      type: {
        in: flowTypeArr
      }
    })
  };

  // Get total count for pagination
  const totalCount = await prisma.tx_flow.count({
    where: whereClause
  });

  // Get paginated data
  const txFlows = await prisma.tx_flow.findMany({
    where: whereClause,
    orderBy: {
      executed_at: 'desc'
    },
    take: take ? Number(take) : undefined,
    skip: cursor ? Number(cursor) : 0
  });

  return NextResponse.json({
    data: txFlows,
    nextCursor: txFlows.length === take ? txFlows[txFlows.length - 1].id : null,
    total: totalCount
  })
}